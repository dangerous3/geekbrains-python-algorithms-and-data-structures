'''Среди натуральных чисел, которые были введены, найти наибольшее по сумме цифр. Вывести на экран это число и сумму
его цифр.'''

# Числа вводятся в новой строке, пока не будет введен 0
n = int(input("Введите число: "))

max_num = 0
max_sum = 0

while n != 0:
    k = n
    sum = 0
    while n > 0:
        sum += n % 10
        n = n // 10
    if sum > max_sum:
        max_sum = sum
        max_num = k
    n = int(input())

